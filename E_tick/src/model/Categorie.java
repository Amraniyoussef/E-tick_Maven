package model;





public class Categorie  {
  
   public int numCategorie;
  
   public String nomCategorie;
 
 
   
   public EvenementCategorie evenementCategorie;

public int getNumCategorie() {
	return numCategorie;
}

public void setNumCategorie(int numCategorie) {
	this.numCategorie = numCategorie;
}

public String getNomCategorie() {
	return nomCategorie;
}

public void setNomCategorie(String nomCategorie) {
	this.nomCategorie = nomCategorie;
}



public EvenementCategorie getEvenementCategorie() {
	return evenementCategorie;
}

public void setEvenementCategorie(EvenementCategorie evenementCategorie) {
	this.evenementCategorie = evenementCategorie;
}





}